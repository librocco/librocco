<script lang="ts">
	import type { FormOptions, SuperValidated } from "sveltekit-superforms";
	import { superForm } from "sveltekit-superforms/client";
	import compare from "just-compare";

	import type { DevSettingsSchema } from "./schemas";
	import { LL } from "@librocco/shared/i18n-svelte";
	import type { Writable } from "svelte/store";

	export let data: SuperValidated<DevSettingsSchema>;
	export let options: FormOptions<DevSettingsSchema>;
	export let active: Writable<boolean>;

	const _form = superForm(data, options);

	const { form: formStore, enhance, tainted } = _form;

	$: hasChanges = $tainted && !compare($formStore, data.data);
</script>

<form class="divide-y-secondary flex flex-col gap-y-10 px-4 py-4" use:enhance method="POST" aria-label={$LL.forms.dev_settings.aria.form()}>
	<div class="flex flex-col justify-between gap-6 lg:flex-row-reverse">
		<div class="flex grow flex-col flex-wrap gap-y-4 lg:flex-row">
			<label class="form-control basis-full">
				<div class="label">
					<span class="label-text">{$LL.forms.dev_settings.labels.translations_url()}</span>
				</div>
				<input id="url" name="translationsUrl" bind:value={$formStore.translationsUrl} class="input-bordered input w-full" />
				<div>
					<p class="label-text">{$LL.forms.dev_settings.labels.translations_url_description_line1()}</p>
					<p class="label-text">{$LL.forms.dev_settings.labels.translations_url_description_line2()}</p>
				</div>
			</label>
		</div>
	</div>
	<div class="flex flex-col justify-between gap-6 lg:flex-row-reverse">
		<div class="flex grow flex-col flex-wrap gap-y-4 lg:flex-row">
			<label class="form-control basis-full">
				<div class="label">
					<span class="label-text">{$LL.forms.dev_settings.labels.translations_api_key()}</span>
				</div>
				<input id="url" name="translationsAPIKey" bind:value={$formStore.translationsAPIKey} class="input-bordered input w-full" />
			</label>
		</div>
	</div>

	<div class="flex items-center justify-between gap-x-2">
		<div class="flex items-center gap-x-2">
			<span class="w-10 text-center {$active ? '' : 'font-bold text-error'}"
				>{$LL.forms.dev_settings.labels.custom_translations_status.disabled()}</span
			>
			<input name="customTranslations" type="checkbox" bind:checked={$active} class="toggle" />
			<span class="w-10 text-center {$active ? 'font-bold text-success' : ''}"
				>{$LL.forms.dev_settings.labels.custom_translations_status.enabled()}</span
			>
		</div>

		<button type="submit" class="btn-primary btn disabled:btn-disabled" disabled={!hasChanges}>
			{$LL.forms.dev_settings.labels.save()}
		</button>
	</div>
</form>
